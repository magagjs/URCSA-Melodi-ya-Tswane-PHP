							<li>Announcements</li>
							<li>Events</li>
							<li>Ward Meetings</li>
							<li>Bearevements</li>
							<li>Newsletters</li>
							<li>Photography during events</li>
							<li>Design of event invitations</li>
						</ul>
						<p>This is a summarized list of congregational activities covered by the MyT Communications Committee. There are 
							activities that may be required by other <a href="congregation.php#committees" target="_blank">committees</a> 
							and <a href="ministries.php" target="_blank">ministries</a> that the Communications Committee may be tasked to cover.</p>
					</section>	
